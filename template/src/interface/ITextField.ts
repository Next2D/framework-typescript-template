/**
 * @description テキストフィールドのインターフェース
 *              Interface for text field
 *
 * @interface
 */
export interface ITextField {
    /**
     * @description テキストフィールドの幅
     *              Width of the text field
     *
     * @type {number}
     */
    width: number;

    /**
     * @description テキストフィールドのX座標
     *              X coordinate of the text field
     *
     * @type {number}
     */
    x: number;
}
